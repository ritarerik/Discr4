

// поиск транзитивных замыканий

public class TransitiveClosure {

	public static int[] getDirect(boolean A[][]) {
		
		int B[] = new int[A[0].length];
		
		for (int i : B) i = 0;
		
		
		return B;
		
	}
	
	public static int[] getReverse(boolean A[][]) {
		
		int B[] = new int[A[0].length];
		
		
		
		return B;
		
	}
	
	
}
